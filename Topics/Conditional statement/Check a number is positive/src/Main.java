import java.util.Scanner;

class Main {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int checkN = scanner.nextInt();
        if (checkN > 0) {
            System.out.println("YES");
        } else if (checkN > 0) {
            System.out.println("NO");
        } else {
            System.out.println("NO");
        }
    }
}
